package br.com.login.huntvision.ws.model;

import javax.ws.rs.FormParam;
import javax.xml.bind.annotation.XmlRootElement;

@SuppressWarnings("serial")
@XmlRootElement(name ="destinatarios")
public final class Destinatario extends RestModel {

	@FormParam("nome")
	private String nome;

	

	@FormParam("email")
	private String email;

	

	@FormParam("itemLocal")
	private ItemLocal itemLocal;


	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Destinatario other = (Destinatario) obj;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		return true;
	}

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the itemLocal
	 */
	public ItemLocal getItemLocal() {
		return itemLocal;
	}

	/**
	 * @param itemLocal the itemLocal to set
	 */
	public void setItemLocal(ItemLocal itemLocal) {
		this.itemLocal = itemLocal;
	}

	public Destinatario(){}

	public Destinatario(String id){
		this.id = Long.valueOf(id);
	}
}